    private void btConcluiCadCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConcluiCadCliActionPerformed
        CliController pc = new CliController();
        Cliente cli = new Cliente(cpf, nome, senha, email, telefone, dataNasc, endereco, numEndereco, bairro, cidade, complemento, unidade_federativa);
        if(tfSenhaCli.getText().equals(tfSenhaRepCli.getText())){
            pc.Incluir(tfCPFCli.getText(), tfNomeCli.getText(), tfSenhaCli.getText(), tfEmailCli.getText(), tfTelefoneCli.getText(), tfDataNascCli.getText(), tfEndCli.getText(), tfNumEndCli.getText(), tfBairroCli.getText(), tfCidadeCli.getText(), tfCompCli.getText(), tfUFCli.getText());
            dispose();
        } else{
            JOptionPane.showMessageDialog(null, "Os campos de senha devem ser iguais.");
            tfSenhaCli.setText("");
            tfSenhaRepCli.setText("");
        }
    }//GEN-LAST:event_btConcluiCadCliActionPerformed

    
